if ($REQUEST_METHOD != 'POST' || !in_array($mode, array('product_modify','pconf')) || empty($efields) || !is_array($efields) || empty($productid)) {
    return;
}

foreach ($efields as $k => $v) {

    // Update extra field value
    func_ef_update_field_value($productid, 0, $k, $v);

}

if (empty($fields['efields']) || empty($geid))
    return;

foreach ($fields['efields'] as $k => $v) {

    $v = $efields[$k];
    while ($pid = func_ge_each($geid, 1, $productid)) {

        // Update extra field value
        func_ef_update_field_value($pid, 0, $k, $v);

    }

}

